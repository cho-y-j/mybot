def generate_blog_tags(
    election_type: str,
    region_short: str,
    candidate_name: str,
) -> dict:
    """
    블로그 관리용 태그 추천 (카테고리별).
    """
    categories = BLOG_TAG_CATEGORIES.get(election_type, {})
    result = {}

    for cat_name, keywords in categories.items():
        tags = []
        for kw in keywords:
            tags.append({
                "tag": kw,
                "variations": [
                    kw,
                    f"{region_short} {kw}",
                    f"{candidate_name} {kw}",
                ],
            })
        result[cat_name] = tags

    return result


